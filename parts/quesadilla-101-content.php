			<h2 class="oil">HISTORY OF THE QUESADILLA</h2>
			<p class="oil">Literally meaning “little cheesy thing,” quesadillas originated in northern and central Mexico in the 16th century.</p>
			<p class="oil">Corn tortillas were already popular among the Aztec people. They often stuffed them with squash and pumpkin and baked them in clay ovens as a sweet dessert. In 1521, Spanish settlers brought sheep, lambs, and cows with them to New Spain, thus introducing indigenous people to cheese and other dairy products. The indigenous people continued stuffing their tortillas with pumpkin and squash, but also added cheese to the mix. Thus, the quesadilla was born.</p>
			<p class="oil">The quesadilla quickly increased in popularity, and to this day has remained a favorite dish in Mexican cuisine.</p>
