					<h3>Enjoy, Wipe the Pan Clean, and Repeat!</h3>
					<p><strong>Tip:</strong> When cooking for a crowd, be sure to continually adding butter to the pan or skillet for each quesadilla you make.</p>
					<p><strong>Tip:</strong> If you are making quesadillas for a large crowd, use a pancake skillet. You’ll thank us—it will make things go a lot faster.</p>
