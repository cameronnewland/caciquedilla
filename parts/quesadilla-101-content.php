			<div class="row">
				<div class="column small-12 large-6">
					<h3>Prepare the Filling</h3>
					<p>Prepare the filling: Pick a few of the suggested filling ingredients above, enough to make 2 to 3 cups of total filling. If combining leftovers, warm them briefly in the microwave or in a skillet over medium heat. If using raw ingredients, cook before making quesadillas. Transfer the filling to a bowl and cover to keep warm.</p>
				</div>
			</div>
		</div>
	</div>
	<div class="quesadilla-panel relative-block panel-2">
		<div class="absolute-block flex-block">
			<div class="row">
				<div class="column small-12 large-6 large-offset-6">
					<h3>Melt ½ teaspoon of butter or oil in the skillet</h3>
					<p>Ironically, the key to a crispy quesadilla is less fat in the pan, not more. Too much fat will make your quesadilla soggy instead of crispy. Use just enough to coat the bottom of your skillet—about 1/2 teaspoon of butter or oil. Warm it in the skillet over medium to medium-high heat.</p>
				</div>
			</div>
		</div>
	</div>
	<div class="quesadilla-panel relative-block panel-3">
		<div class="absolute-block flex-block">
			<div class="row">
				<div class="column small-12 large-6">
					<h3>Add the tortilla and top with cheese</h3>
					<p>Lay one tortilla in the skillet and sprinkle all over with 1/2 cup of cheese. Don’t feel obliged to use flour or corn. Expand your horizons to spinach or red pepper infused tortillas. If you have a little more time on your hands, make the tortillas yourself!</p>
					<p><strong>Tip:</strong> Double check that your tortilla is large enough to fit all of the filling into it. 9’’–10’’ tortillas are usually best.</p>
				</div>
			</div>
		</div>
	</div>
	<div class="quesadilla-panel relative-block panel-4">
		<div class="absolute-block flex-block">
			<div class="row">
				<div class="column small-12 large-6 large-offset-6">
					<h3>Add the filling</h3>
					<p>Spread roughly 1/2 cup of filling in a single layer over just half of the tortilla. Don't use too much or the filling will fall out as you try to eat it. Spreading the filling over half makes the quesadilla easier to fold, and adding it as the cheese melts gives the filling time to warm if it has cooled.</p>
				</div>
			</div>
		</div>
	</div>
	<div class="quesadilla-panel relative-block panel-5">
		<div class="absolute-block flex-block">
			<div class="row">
				<div class="column small-12 large-6">
					<h3>Watch for the cheese to melt</h3>
					<p>Once the cheese starts to melt, begin lifting a corner of the tortilla and checking the underside. When the cheese has completely melted and you see golden-brown spots on the underside of the tortilla, the quesadilla is ready.</p>
					<p><strong>Tip:</strong> Be patient! The quesadilla will grill better and a more even melt on medium heat.</p>
				</div>
			</div>
		</div>
	</div>
	<div class="quesadilla-panel relative-block panel-6">
		<div class="absolute-block flex-block">
			<div class="row">
				<div class="column small-12 large-6 large-offset-6">
					<h3>Fold the quesadilla in half</h3>
					<p>Use the spatula to fold the quesadilla in half, sandwiching the filling.</p>
				</div>
			</div>
		</div>
	</div>
