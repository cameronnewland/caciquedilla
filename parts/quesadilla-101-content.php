			<div class="row">
				<div class="column small-12 large-6">
					<h3>Transfer to a cutting board and slice into wedges</h3>
					<p>Slide the hot quesadilla onto a cutting board. If you are serving (or eating) the quesadilla immediately, slice into wedges. Using a pizza sliver always helps!</p>
					<p><strong>Tip:</strong> If you are not serving it immediately, slide the quesadilla onto a baking sheet and keep it in a 200 degree oven. </p>
					<p><strong>Tip:</strong> For optimal tastiness, only slice the quesadilla into wedges immediately before serving. </p>
				</div>
